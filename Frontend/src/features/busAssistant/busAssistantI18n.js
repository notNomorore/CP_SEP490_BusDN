export const translateBusAssistantError = (error, language, fallback = '') => {
  const message = typeof error === 'string' ? error : error?.message;
  const normalized = String(message || fallback || '').trim();

  if (!normalized || language !== 'vi') {
    return normalized;
  }

  return busAssistantErrorTranslations.vi[normalized] || normalized;
};

